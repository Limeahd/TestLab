package am;

public class Demo4 {
    /**
     * 比较运算符
     * @param args
     */
    public static void main(String[] args) {
        System.out.println(5>3);
        System.out.println(5<3);
        System.out.println(5>=3);
        System.out.println(5<=3);
        System.out.println(5==3);
        System.out.println(5!=3);
    }
}
